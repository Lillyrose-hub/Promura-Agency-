// Function to replace emoji placeholders with SVG icons
function replaceEmojisWithIcons() {
    // Map of emoji to icon name
    const emojiMap = {
        '🕐': 'clock',
        '⚡': 'zap',
        '✅': 'check',
        '❌': 'x',
        '✏️': 'edit',
        '🗑️': 'trash',
        '🎥': 'video',
        '📅': 'calendar',
        '📈': 'analytics',
        '👀': 'eye',
        '❤️': 'heart',
        '➕': 'plus',
        '×': 'removeX'
    };

    // Replace emojis in specific elements
    document.querySelectorAll('.status-icon, .meta-item, .action-btn').forEach(element => {
        const text = element.textContent || element.innerHTML;
        Object.keys(emojiMap).forEach(emoji => {
            if (text.includes(emoji)) {
                const iconName = emojiMap[emoji];
                if (icons[iconName]) {
                    // For action buttons, just replace content
                    if (element.classList.contains('action-btn')) {
                        element.innerHTML = icons[iconName];
                    } else {
                        // For other elements, replace the emoji but keep other text
                        element.innerHTML = element.innerHTML.replace(emoji, icons[iconName]);
                    }
                }
            }
        });
    });

    // Replace video icon placeholders
    document.querySelectorAll('.video-icon').forEach(element => {
        element.innerHTML = icons.video;
    });
}

// Initialize on DOM load
document.addEventListener('DOMContentLoaded', replaceEmojisWithIcons);

// Export for use in other scripts
window.dashboardIcons = icons;